                                    <h2>O Escritório</h2>
                                    <hr/>
                                    <p>O escritório CANDIDO DE OLIVEIRA tem larga tradição nos meios jurídicos. Foi fundado em 1891 pelo Conselheiro CANDIDO DE OLIVEIRA, natural de Ouro Preto, onde nasceu em 1845.</p>
                                    <p>Formado nas Arcadas do Largo de São Francisco, hoje a USP, exerceu relevantes cargos públicos durante o Império. Foi Magistrado, Deputado, Senador e Ministro da Justiça, quando presidiu a Comissão encarregada da elaboração do nosso primeiro Código Civil. Com a proclamação da República passou a se dedicar exclusivamente à advocacia e ao magistério, lecionando e depois dirigindo a Faculdade Livre de Direito, atual UFRJ. Em virtude dessas atividades foi escolhido patrono do seu Centro Acadêmico, que por isso leva o nome de Centro Acadêmico Candido de Oliveira – CACO.</p>
                                    <p>CANDIDO DE OLIVEIRA FILHO seguiu os passos do pai na advocacia, dedicando-se juntamente com ele ao escritório. Teve ainda intensa vida acadêmica e fundou a Livraria Editora Candido de Oliveira, responsável pela publicação de inúmeros e importantes livros de Direito na primeira metade do século passado. Sagrou-se Professor Emérito da Faculdade Nacional de Direito da Universidade do Brasil e Grã-Cruz da Ordem de Instrução Pública de Portugal.</p>
                                    <p>Seu sucessor no escritório, CANDIDO DE OLIVEIRA NETO, além de advogado atuante, exerceu também os cargos de Procurador Geral do Distrito Federal, de Procurador Geral da República e de Ministro da Justiça. Foi Conselheiro da Ordem dos Advogados do Brasil e Vice-Presidente do Instituto dos Advogados Brasileiros.</p>
                                    <p>Hoje, CANDIDO DE OLIVEIRA BISNETO, formado pela Pontifícia Universidade Católica do Rio de Janeiro em 1960, segue a tradição, dedicando-se exclusivamente à advocacia. Foi conselheiro por vários anos da Ordem dos Advogados do Brasil no Rio de Janeiro – OAB/RJ, instituição da qual foi Diretor por vários biênios e depois Presidente no biênio de 1989/1990. Atualmente é Presidente da Comissão de Admissão de Sócios do Instituto dos Advogados Brasileiros – IAB e Membro do seu Conselho Superior.</p>
